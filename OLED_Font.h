void fillBufByCharBitmap(byte charBuf[], byte code, bool invert)
// Function fills charBuf[] by 48 bytes character pixel data from charMap[]
{
	int charMapIndex = 0;

	if (32 <= code && code <= 126)
	// Default ASCII characters
	{
		charMapIndex = (code - 32)*48;
	} 

	else 
	{
		if (code == 0x00)
		{
			fillBufByCharBitmap(charBuf, ' ', invert);
		}

		if (code >= 192)
		// CP-1251 cyrillic characters
		{
			charMapIndex = (code - 97)*48;
		}

		switch(code) {
			// 'І'
			case 178:
				charMapIndex = 7632;
				break;

			// 'і'
			case 179:
				charMapIndex = 7680;
				break;

			// 'Ї'
			case 175:
				charMapIndex = 7728;
				break;

			// 'ї'
			case 191:
				charMapIndex = 7776;
				break;

			// 'Ґ'
			case 165:
				charMapIndex = 7824;
				break;

			// 'ґ'
			case 180:
				charMapIndex = 7872;
				break;

			// 'Є'
			case 170:
				charMapIndex = 7920;
				break;

			// 'є'
			case 186:
				charMapIndex = 7968;
				break;
		}
	}


	for (int i = 0; i < 48; i++)
	{
		byte charByte = pgm_read_byte(&charMap[charMapIndex + i]);
		charBuf[i] = invert ? ~charByte: charByte;
	}

};

